        <h3>¿Puedo comprar por la página y recoger en almacén?</h3>
        <p>Si. Los clientes tienen la opción de comprar productos y elegir la opción de recoger en el almacén. Esta opción está disponible en el proceso de pago, bajo la opción llamada: RECOGER EN TIENDA.</p>
        <h3>¿Puedo cambiar mi dirección de entrega luego de realizar mi pedido?</h3>
